CREATE TABLE manager
(
    id                SERIAL              NOT NULL,
    user_profile_id   TEXT                NOT NULL,
    invitation_status VARCHAR(50)         NOT NULL,
    function          VARCHAR(50)         NOT NULL,
    firstname         VARCHAR(50)         NOT NULL,
    lastname          VARCHAR(50)         NOT NULL,
    archived          BOOLEAN DEFAULT '0' NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE nace
(
    id    SERIAL       NOT NULL,
    code  VARCHAR(50)  NOT NULL,
    label VARCHAR(150) NOT NULL,
    PRIMARY KEY (id),
    UNIQUE (code)
);

CREATE TABLE location_type
(
    id          serial not null,
    external_id varchar(500),
    PRIMARY KEY (id)
);


CREATE TABLE operating_mode
(
    id        SERIAL NOT NULL,
    shape_url VARCHAR(2083),
    svg_path  VARCHAR(2083),
    code      VARCHAR(50),
    PRIMARY KEY (id),
    UNIQUE (code)
);

CREATE TABLE concept
(
    id   SERIAL NOT NULL,
    code VARCHAR(50),
    PRIMARY KEY (id),
    UNIQUE (code)
);

CREATE TABLE states
(
    id         SERIAL  NOT NULL,
    country_id INTEGER NOT NULL REFERENCES country (id),
    name       VARCHAR(250),
    PRIMARY KEY (id),
    UNIQUE (name)
);

CREATE TABLE zone
(
    id              SERIAL NOT NULL,
    code            VARCHAR(50),
    zone_manager_id INTEGER REFERENCES manager (id),
    PRIMARY KEY (id),
    UNIQUE (code)
);

CREATE TABLE region
(
    id                  SERIAL NOT NULL,
    regional_manager_id INTEGER REFERENCES manager (id),
    brand_id            INTEGER REFERENCES brand (id),
    code                VARCHAR(50),
    zone_id             INTEGER REFERENCES zone (id),
    country_id          INTEGER REFERENCES country (id),
    PRIMARY KEY (id),
    UNIQUE (code)
);

CREATE TABLE indexation_month
(
    id         SERIAL NOT NULL,
    month_name VARCHAR(50),
    PRIMARY KEY (id)
);

